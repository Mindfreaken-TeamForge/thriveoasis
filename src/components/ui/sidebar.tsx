interface SidebarMenuButtonProps
  extends React.HTMLAttributes<HTMLDivElement> {
  size?: "default" | "lg"
  onClick?: () => void
}

const SidebarMenuButton = React.forwardRef<
  HTMLDivElement,
  SidebarMenuButtonProps
>(({ className, size = "default", onClick, ...props }, ref) => (
  <div
    ref={ref}
    role="button"
    tabIndex={0}
    onClick={onClick}
    onKeyDown={(e) => {
      if (e.key === 'Enter' || e.key === ' ') {
        onClick?.()
      }
    }}
    className={cn(
      "flex w-full items-center gap-2 rounded-lg px-4 py-2 transition-all duration-300 cursor-pointer",
      size === "default" && "h-10",
      size === "lg" && "h-14",
      className
    )}
    {...props}
  />
))
SidebarMenuButton.displayName = "SidebarMenuButton"

